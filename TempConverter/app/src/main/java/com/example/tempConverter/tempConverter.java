package com.example.tempConverter;

public class tempConverter {
    public float F;
    public float C;
    public tempConverter() {}

    public void setC(float degC) {
        this.C = degC;
    }

    public float getC() {
        return this.C;
    }

    public void setF(float degF) {
        this.F = degF;
    }

    public float getF() {
        return this.F;
    }
}
